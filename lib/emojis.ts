/**
 * Search for emojis based on query
 * @param query - Search term
 * @returns Array of matching emojis
 */
export function searchEmojis(query: string): Emoji[] {
  if (!query) return emojis.slice(0, 20); // Return first 20 emojis if no query

  const searchTerm = query.toLowerCase().trim();
  const results: { emoji: Emoji; score: number }[] = [];

  emojis.forEach((emoji) => {
    let score = 0;

    // Exact name match gets highest score
    if (emoji.name === searchTerm) {
      score = 1;
    }
    // Name starts with query
    else if (emoji.name.startsWith(searchTerm)) {
      score = 2;
    }
    // Name contains query
    else if (emoji.name.includes(searchTerm)) {
      score = 3;
    }
    // Keywords exact match
    else if (emoji.keywords.includes(searchTerm)) {
      score = 4;
    }
    // Keywords start with query
    else if (emoji.keywords.some((keyword) => keyword.startsWith(searchTerm))) {
      score = 5;
    }
    // Keywords contain query
    else if (emoji.keywords.some((keyword) => keyword.includes(searchTerm))) {
      score = 6;
    }

    if (score > 0) {
      results.push({ emoji, score });
    }
  });

  // Sort by score and return emojis
  return results
    .sort((a, b) => a.score - b.score)
    .slice(0, 20) // Limit to 20 results
    .map((result) => result.emoji);
}

/**
 * Get emojis by category
 * @param category - Category name
 * @returns Array of emojis in that category
 */
export function getEmojisByCategory(category: string): Emoji[] {
  return emojis.filter((emoji) => emoji.category === category);
}

/**
 * Get all available categories
 * @returns Array of category names
 */
export function getEmojiCategories(): string[] {
  const categories = new Set(emojis.map((emoji) => emoji.category));
  return Array.from(categories);
}

/**
 * Get a random emoji
 * @returns Random emoji
 */
export function getRandomEmoji(): Emoji {
  return emojis[Math.floor(Math.random() * emojis.length)];
}

